                        <h2 class="secTitle">Admission Process</h2>
    
                        <h3 class="widgetTitle text-dark fs-5 mb-3">Admission</h3>
                        <p>It is advised to plan a visit to the school campus after seeking an appointment with the School Admission Office and familiarize yourself in regard to all the rules, facets, aspects and quality of the school.</p>
                        <p>The admission procedure starts with Registration of the child for admission.</p>
                        <p>Please note that Registration does not guarantee admission.</p>
    
                        <h3 class="widgetTitle text-dark fs-5 mb-3">Registration</h3>
                        <p>Registration is normally open throughout the year. Both NRI and resident Indian children are to be registered. Registration entails filling in the requisite “Admission Form”, submitting the applicable documents and remitting the requisite registration fee,  through registered post or by hand in the School Office. (Registration Fee and all other applicable School Fee is mentioned in the “Fee Schedule”  Section).</p>
    
                        <h3 class="widgetTitle text-dark fs-5 mb-3">Important Note</h3>
                        <p>It is imperative to understand that reasonably good academic competence is necessary, for a child to be able to perform well in a boarding school environment, where the co-curricular and activity curriculum doesn't allow extra-time for academic pursuit and lots of time is consumed in building personality, value systems, confidence, independence, self-reliance and self-dependence, which are of paramount importance. </p>
                        <p>Hence, you need to plan to send a child to a hostel only if you think your child will be able to come to these expected levels independently. Keeping this in view, the procedure has been framed to gauge the general intelligence and academic standard of the child. There is no particular syllabus required for the boarding school orientation. It would consist of the previous class syllabi based on the National NCERT parameters and guidelines.</p>
                        <p>Students applying for Classes II - VI are required to visit the Dharampur Campus, on a date scheduled by the School's Admission Office, for a Boarding Orientation Programme and for students applying for Classes VII, VIII, IX and XI shall be attend the “Boarding School Orientation Programme” held at the Subathu campus, on a stipulated date, in early February. This date is mentioned in the school calendar.</p>
                        <p>All new admissions must join the school after successful completion of their academic session in the previous school and obtaining proper Transfer Certificate/ School Leaving Certificates, as per formats authorized by the CBSE /ICSE/State Board.</p>
    
                        <h3 class="widgetTitle text-dark fs-5 mb-3">NRI Admissions</h3>
                        <p>All parents who are NRIs or earning in foreign exchange and those whose children are not Indian nationals shall be considered as NRI children.</p>
                        <p>Please note that NRI students (seeking admission in Classes VI and VII) are granted direct admission, subject to availability of seats, and are excused from attending the “Orientation Programme” in person. This is permitted if the child is flying in from abroad for the sole purpose of seeking admission. NRI children seeking admission in Classes VIII and above will need to attend the “Boarding School Orientation Programme”.</p>
                        <p>The standard rule being that the admission will be granted provisionally for the first year and further continuance will depend on the child's performance, academic and otherwise, during the first year of his/her stay in the school.</p>
                        <p>As an NRI, it is advisable to get the child admitted only if you have some relative/friend who can be the local guardian, for visiting your child on visiting weekends, short/long holidays and shoulder responsibility, especially in case of emergencies.</p>
    
                        <h3 class="widgetTitle text-dark fs-5 mb-3">Vacancies</h3>
                        <p>Vacancies are generally available for both boys and girls. For Classes VI and VII at Dharampur or Subathu location of Pinegrove School, depending upon the availability of seats and in Classes VIII, IX and XI admission is available only at Pinegrove School, Subathu.</p>
    
                        <h3 class="widgetTitle text-dark fs-5 mb-3">Admission</h3>
                        <p>The list of successful candidates is declared after the Orientation, in the month of February and the admission needs to be accepted/ declined within a week from this declaration.</p>
                        <p>Admissions are granted with due preference to NRIs, brothers/sisters of present Grovians, brothers/sisters/children of Old Grovians, children of serving defence personnel, war widows, Govt Employees and brilliant scholarship/free-ship students from the poorer section of society.</p>
    
                        <h3 class="widgetTitle text-dark fs-5 mb-3">Age Criteria</h3>
                        <p>Admission, as per the NEP 2020, need to be offered in Class I (1st Grade) for a child aged between 6 to 7  (six to seven years) as on 31st March and for Class VI and child should be aged between  12-13 years as on 31st March of the year of admission. The age criteria for the subsequent classes may be calculated. Generally, the accepted formula is, age of child minus 6 = Class to be admitted.</p>
    
                        <h3 class="widgetTitle text-dark fs-5 mb-3">Plus-2 Admission</h3>
                        <ol style="list-style-type: decimal;margin-left: 18px;" class="mb-3">
                            <li>Rights of admission to the Senior Secondary Stage (Plus 1 & 2) are reserved. </li>
                            <li>New Admissions for Class XI shall follow the procedure of Registration, Admission Test and interview.</li>
                            <li>Children passing out of the secondary stage from Pinegrove will not have any right for direct admission to the Senior Secondary Stage and will need to reapply for admission for the senior secondary stage.</li>
                            <li>Due to very limited seats, preference for admission will be given to students passing out from the 10th class from Pinegrove School itself. Admission to Class XI, may not be offered, dependent on limited availability of seats in the hostel or on conduct and academic performance of the child during the previous stay of the child in the school. Existing students shall be considered for admission and be offered provisional admission, on the basis of the result card of the 9th Class Final Exams and the Pre-Board Result of 10th Class. The cut off percentage for admission will be announced each year as per school rules. Appropriate weightage shall be given to students excelling in sports and at the discretion of the school management.</li>
                            <li>No re-admission fee will be charged, from existing students, if the request for admission reaches the School Office by the scheduled date, as announced by the school authorities.</li>
                            <li>In the best interest of the child no reasons for rejection need be given and the decision of the School Management and/or the Head of School will be final and binding.</li>
                            <li>All admissions in Class XI, for new and for existing students, will be provisional till such time the Secondary School Board results are not declared. The provisional admission shall be confirmed after the senior secondary board result. The provisional admission will be withdrawn in such cases where the minimum percentage is not obtained or if a child gets a Compartment or a Fail result in the Secondary School Board results.</li>
                        </ol>
    
                        <h3 class="widgetTitle text-dark fs-5 mb-3">Documents to be submitted at the time of registration</h3>
                        <p>For the purpose of registration parents are required to submit the following:</p>
                        <ol style="list-style-type: lower-alpha;margin-left: 18px;" class="mb-3">
                            <li>Birth Certificate (attested photocopy).</li>
                            <li>Attested copy of the Aadhar Card of the child.</li>
                            <li>Two coloured passport size latest photographs of the child</li>
                            <li>Last two-year academic record/ mark sheet of the child.</li>
                            <li>Pre-Board Marks Sheet of the previous School (for +2 Stage students only).</li>
                            <li>Declaration of Medical Fitness </li>
                            <li>Registration fee payable as per fee schedule.</li>
                            <li>Duly filled in Admission Form along with the signed copy of School Rules.</li>
                            <li>(Additional documents for +2 Stage students are listed after a few paragraphs)</li>
                        </ol>
    
                        <h3 class="widgetTitle text-dark fs-5 mb-3">Additional Documents to be submitted at the time of Admission</h3>
                        <p>In addition to the documents submitted at the time of registration, the following are to be submitted at the time of admission for all students:</p>
                        <ul class="mb-3" style="list-style-type:lower-alpha;margin-left:18px;">
                            <li>Original School Leaving Certificate or Transfer Certificate</li>
                            <li>Photocopy of the Report Card of previous class passed.</li>
                            <li>Medical Certificate as per school format </li>
                        </ul>
    
                        <h3 class="widgetTitle text-dark fs-5 mb-3">Additional Documents for NRIs</h3>
                        <p>In addition to the above mentioned documents, the following documents will also have to be submitted by NRI and foreign students:</p>
                        <ul class="mb-3" style="list-style-type: lower-alpha;margin-left: 18px;">
                            <li>Original Passport with valid STUDENT VISA (in original).</li>
                            <li>An additional attested photocopy of Passport</li>
                            <li>Letter of acceptance from the local guardian agreeing to be the guardian of the child during his/her stay at the school and accepting to take full responsibility and care of the child during short leaves, vacations and especially during emergencies.</li>
                            <li>Copy of the Police report regarding the permission for stay of over six months in India. (FRRO Registration, if the child is above 12 years of age).</li>
                            <li>Letter of Authority permitting the school authorities or its representatives to take all responsibility in case of any emergency.</li>
                            <li>Photographs and addresses of people allowed to meet the child/children and to take them on visiting weekends and vacations. (for NRI students only)</li>
                        </ul>
    
                        <h3 class="widgetTitle text-dark fs-5 mb-3">Additional Documents required to be submitted for Plus-2 admission</h3>
                        <p>Documents to be submitted by children seeking admission to the Senior Secondary Stage at the time of admission will be as follows:</p>
                        <ul class="mb-3" style="list-style-type: lower-alpha;margin-left: 18px;">
                            <li>Duly filled in Admission Form for Class XI</li>
                            <li>Report Card of Class IX (Final)</li>
                            <li>Report Card of Class X (Pre-Board)</li>
                            <li>Supporting Documents of achievements in Sports/other activities</li>
                        </ul>
    
                        <p>Documents to be submitted at the time of admission in Class XI:</p>
                        <ul class="mb-3" style="list-style-type: lower-alpha;margin-left: 18px;">
                            <li>Transfer Certificate from the previous School (As per CBSE/ ICSE/State Board format)</li>
                            <li>Character Certificate and Proof of Birth from the School</li>
                            <li>Medical Certificate as per School Format </li>
                        </ul>
    
                        <p>Documents to be submitted immediately after declaration of result by Secondary School Board:</p>
                        <ul class="mb-3" style="list-style-type: lower-alpha;margin-left: 18px;">
                            <li>Migration Certificate from the Board</li>
                            <li>Secondary School Board Marks/Statement/ of Class X (in Original)</li>
                        </ul>
    
